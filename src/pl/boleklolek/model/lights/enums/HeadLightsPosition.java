package pl.boleklolek.model.lights.enums;

/**
 * Ta klasa enum określa pozycję świateł głównych.
 */
public enum HeadLightsPosition
{
    /**
     * Światła wyłączone.
     */
    OFF,
    /**
     * Światła pozycyjne (dzienne).
     */
    DAILY,
    /**
     * Światła mijania.
     */
    LOWBEAM,
    /**
     * Światła drogowe.
     */
    HIGHBEAM
}
